use crate::kernel::native_graphics::Resolution;

const CHAR_WIDTH: usize = 8;
const CHAR_HEIGHT: usize = 16;
pub(super) const CHAR_RESOLUTION: Resolution = Resolution {
    horizontal: CHAR_WIDTH,
    vertical: CHAR_HEIGHT,
};

pub(super) const CHAR_COUNT: usize = 256;

#[derive(Clone, Copy, Debug)]
pub enum CharBit {
    Foreground,
    Background,
}

impl From<bool> for CharBit {
    fn from(is_foreground: bool) -> Self {
        if is_foreground {
            Self::Foreground
        } else {
            Self::Background
        }
    }
}

#[derive(Clone, Copy, Debug)]
pub(super) struct CharBitLine(u8);

impl CharBitLine {
    pub(super) fn get_bits(&self) -> [CharBit; CHAR_WIDTH] {
        [
            CharBit::from(self.0 & 128 != 0),
            CharBit::from(self.0 & 64 != 0),
            CharBit::from(self.0 & 32 != 0),
            CharBit::from(self.0 & 16 != 0),
            CharBit::from(self.0 & 8 != 0),
            CharBit::from(self.0 & 4 != 0),
            CharBit::from(self.0 & 2 != 0),
            CharBit::from(self.0 & 1 != 0),
        ]
    }
}

#[derive(Clone, Copy, Debug)]
pub(super) struct CharBitMap([CharBitLine; CHAR_HEIGHT]);

impl CharBitMap {
    pub(super) fn get_lines(&self) -> [CharBitLine; CHAR_HEIGHT] {
        self.0
    }
}

impl From<char> for CharBitMap {
    fn from(c: char) -> Self {
        let mut buffer = [0u8; 4];
        let as_utf8 = c.encode_utf8(&mut buffer);
        let as_byte = if as_utf8.len() == 1 { buffer[0] } else { b' ' };
        let start_index = as_byte as usize * CHAR_HEIGHT;
        CharBitMap([
            CharBitLine(CHAR_BITMAPS[start_index]),
            CharBitLine(CHAR_BITMAPS[start_index + 1]),
            CharBitLine(CHAR_BITMAPS[start_index + 2]),
            CharBitLine(CHAR_BITMAPS[start_index + 3]),
            CharBitLine(CHAR_BITMAPS[start_index + 4]),
            CharBitLine(CHAR_BITMAPS[start_index + 5]),
            CharBitLine(CHAR_BITMAPS[start_index + 6]),
            CharBitLine(CHAR_BITMAPS[start_index + 7]),
            CharBitLine(CHAR_BITMAPS[start_index + 8]),
            CharBitLine(CHAR_BITMAPS[start_index + 9]),
            CharBitLine(CHAR_BITMAPS[start_index + 10]),
            CharBitLine(CHAR_BITMAPS[start_index + 11]),
            CharBitLine(CHAR_BITMAPS[start_index + 12]),
            CharBitLine(CHAR_BITMAPS[start_index + 13]),
            CharBitLine(CHAR_BITMAPS[start_index + 14]),
            CharBitLine(CHAR_BITMAPS[start_index + 15]),
        ])
    }
}

